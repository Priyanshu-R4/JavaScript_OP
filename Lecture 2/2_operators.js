// Arithmetic Operators in JavaScript

console.log(2 + 22, "Addition");
console.log(2 - 22, "Subtraction");
console.log(2 * 22, "Multiplication");
console.log(22 / 2, "Division");
console.log(2 % 22, "Modulus (Remainder)");
console.log(2 ** 5, "Exponentiation (2 to the power 5)");

// Increment and Decrement need variables
let a = 1;
console.log(++a, "Increment (prefix)"); // adds 1 before using
console.log(a++, "Increment (postfix)"); // uses then adds 1
console.log(--a, "Decrement (prefix)");
console.log(a--, "Decrement (postfix)");